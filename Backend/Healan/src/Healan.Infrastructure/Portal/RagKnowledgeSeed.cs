public static class RagKnowledgeSeed
{
    private sealed record SeedRow(
        string Question,
        string Summary,
        string Keywords,
        string Topic,
        string Answer,
        string Similar,
        int SortOrder,
        int Priority = 0);

    public static async Task SeedAsync(ApplicationDbContext context)
    {
        await SeedSettingsAsync(context);
        if (await context.RagKnowledgeItems.AnyAsync())
            return;

        var rows = BuildSeedRows();
        foreach (var row in rows)
        {
            context.RagKnowledgeItems.Add(new RagKnowledgeItem
            {
                Question = row.Question,
                QuestionSummary = row.Summary,
                Keywords = row.Keywords,
                Topic = row.Topic,
                Answer = row.Answer,
                SimilarQuestions = row.Similar,
                SortOrder = row.SortOrder,
                Priority = row.Priority,
                IsActive = true,
                SearchText = RagSearchTextBuilder.Build(
                    row.Question, row.Summary, row.Keywords, row.Topic, row.Similar),
            });
        }

        await context.SaveChangesAsync();
    }

    private static async Task SeedSettingsAsync(ApplicationDbContext context)
    {
        var dockerUrl = "http://python-rag:8000";
        if (await context.RagSettings.AnyAsync())
        {
            // Existing installs often still point at localhost — rewrite for Docker.
            var setting = await context.RagSettings.FirstAsync();
            var url = setting.PythonApiUrl?.Trim() ?? string.Empty;
            var changed = false;
            if (url.Contains("localhost:8000", StringComparison.OrdinalIgnoreCase)
                || url.Contains("127.0.0.1:8000", StringComparison.OrdinalIgnoreCase)
                || string.IsNullOrWhiteSpace(url))
            {
                setting.PythonApiUrl = dockerUrl;
                changed = true;
            }
            if (string.IsNullOrWhiteSpace(setting.EmbeddingModel))
            {
                setting.EmbeddingModel = "heydariAI/persian-embeddings";
                changed = true;
            }
            if (string.IsNullOrWhiteSpace(setting.SummarizeModel))
            {
                setting.SummarizeModel = "qwen2.5:3b";
                changed = true;
            }
            if (changed)
            {
                setting.UpdatedAt = DateTime.UtcNow;
                await context.SaveChangesAsync();
            }
            return;
        }

        context.RagSettings.Add(new RagSetting
        {
            RagSettingId = 1,
            SyncIntervalMinutes = 10,
            SimilarityThresholdPercent = 55,
            PythonApiUrl = dockerUrl,
            IsEnabled = true,
            GuestDailyLimit = 10,
            AuthenticatedDailyLimit = 200,
            EmbeddingModel = "heydariAI/persian-embeddings",
            SummarizeModel = "qwen2.5:3b",
            UpdatedAt = DateTime.UtcNow,
        });
        await context.SaveChangesAsync();
    }

